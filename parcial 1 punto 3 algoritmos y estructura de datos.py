def ordenar_jedi_por_nombre_y_especie(lista_jedi):
    # Ordena la lista por nombre
    lista_jedi.sort(key=lambda x: x['name'])

    # Ordena la lista por especie dentro de cada grupo de Jedi con el mismo nombre
    lista_jedi.sort(key=lambda x: x['species'] if x['species'] else '')

    return lista_jedi

# Uso de ejemplo:
jedi_ordenados = ordenar_jedi_por_nombre_y_especie(jedis)
for jedi in jedi_ordenados:
    print(jedi['name'], jedi['species'])

def mostrar_info_jedi(nombre_jedi, lista_jedi):
    for jedi in lista_jedi:
        if jedi['name'] == nombre_jedi:
            print("Información de", nombre_jedi + ":")
            for key, value in jedi.items():
                print(f"{key}: {value}")
            print()

# Mostrar la información de Ahsoka Tano y Kit Fisto
mostrar_info_jedi("Ahsoka Tano", jedis)
mostrar_info_jedi("Kit Fisto", jedis)


def mostrar_padawan_maestro(nombre_maestro, lista_jedi):
    padawans = []
    for jedi in lista_jedi:
        if jedi['master'] == nombre_maestro and jedi['rank'] == 'Padawan':
            padawans.append(jedi['name'])
    return padawans

# Mostrar los padawan de Yoda
padawans_yoda = mostrar_padawan_maestro("Yoda", jedis)
print("Padawan de Yoda:")
for padawan in padawans_yoda:
    print(padawan)

# Mostrar los padawan de Luke Skywalker
padawans_luke = mostrar_padawan_maestro("Luke Skywalker", jedis)
print("\nPadawan de Luke Skywalker:")
for padawan in padawans_luke:
    print(padawan)


def mostrar_jedi_por_especie(especie, lista_jedi):
    jedi_especie = []
    for jedi in lista_jedi:
        if jedi['species'] == especie:
            jedi_especie.append(jedi)
    return jedi_especie

# Mostrar los Jedi de especie humana
jedi_humanos = mostrar_jedi_por_especie("Human", jedis)
print("Jedi de especie humana:")
for jedi in jedi_humanos:
    print(jedi['name'])

# Mostrar los Jedi de especie Twi'lek
jedi_twi_lek = mostrar_jedi_por_especie("Twi'lek", jedis)
print("\nJedi de especie Twi'lek:")
for jedi in jedi_twi_lek:
    print(jedi['name'])



def listar_jedi_por_inicial(letra, lista_jedi):
    jedi_con_inicial = []
    for jedi in lista_jedi:
        if jedi['name'].startswith(letra):
            jedi_con_inicial.append(jedi['name'])
    return jedi_con_inicial

# Listar todos los Jedi cuyos nombres comienzan con "A"
jedi_con_a = listar_jedi_por_inicial("A", jedis)
print("Jedi cuyos nombres comienzan con 'A':")
for jedi in jedi_con_a:
    print(jedi)


